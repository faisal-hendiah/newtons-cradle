/**
 * ============================================================================
 * PhysicsMath Class (Static Utility)
 * ============================================================================
 * يوفر هذا الكلاس الدوال الرياضية المجردة لحل تفاعلات الأجسام.
 * مسؤوليته:
 * 1. تطبيق مصفوفة تبادل السرعات (1D Elastic Collision).
 * 2. معالجة التداخل البصري بين الكرات (Penetration Resolution).
 * ============================================================================
 */
export class PhysicsMath {
  /**
   * دالة حل التصادم المرن التام في بعد واحد
   * تعتمد على قانوني: انحفاظ كمية الحركة (Momentum) وانحفاظ الطاقة الحركية (Kinetic Energy)
   */
  static resolveElasticCollision(ball1, ball2, restitution) {
    // 1. تحويل السرعة الزاوية إلى سرعة خطية مماسية لحظة الصدم
    /**
     * نحول الى سرعة خطية لانخ لحظة الصدم تكون حركة الكرات هي حركة مستقيمة وليست دائرية.
     * أيضا قانون انحفاظ كمية الحركة يتعامل مع السرعة الخطية وليس الزاوية.
     * P = m . v
     */
    // القانون: v = ω * r or v = ω * L
    // حيث  r هو نصف قطر مسار الدوران وليس نصف قطر الكرة

    const v1 = ball1.omega * ball1.length;
    const v2 = ball2.omega * ball2.length;

    // 2. التحقق من اتجاه الحركة: هل الكرتان تتقاربان أم تتباعدان؟
    // إذا كانت v1 <= v2، فهما تبتعدان ولا داعي لتطبيق الصدم (يمنع التلاصق الوهمي)

    /**
     * هذا الشرط يمنع الالتصاق الدائم للكرات ففي حالة كرة 1 هابطة من الاعلى تصطدم بالكرة 2 بالاسفل
     * فيحدث الاصطدام وتنتقل سرعة الكرة الاولى الى الكرة الثانية فتصبح سرعتتها اكبر ويتحقق الشرط ويتوقف الصدم
     */
    if (v1 <= v2) return false;

    const m1 = ball1.mass;
    const m2 = ball2.mass;

    // 3. حساب كمية الحركة الكلية للنظام (Total Momentum)
    // القانون: P_total = m1*v1 + m2*v2
    const totalMomentum = m1 * v1 + m2 * v2;

    // 4. معادلة الصدم 1D العامة (مع معامل الارتداد 'e')
    // السرعة الجديدة للجسم الأول
    const v1_new = (totalMomentum - m2 * restitution * (v1 - v2)) / (m1 + m2);
    // السرعة الجديدة للجسم الثاني
    const v2_new = (totalMomentum + m1 * restitution * (v1 - v2)) / (m1 + m2);

    // 5. إعادة تحويل السرعة الخطية الناتجة إلى سرعة زاوية للمتابعة في المسار الدائري
    ball1.omega = v1_new / ball1.length;
    ball2.omega = v2_new / ball2.length;

    return true; // تمت عملية الصدم بنجاح
  }

  /**
   * دالة فك التداخل الميكانيكي (Static Collision Resolution)
   * تستخدم لمنع الكرات من اختراق بعضها البعض في الرسوميات.
   */
  static resolveOverlap(ball1, ball2, minDistance, currentDistance) {
    // حساب مقدار الاختراق (Penetration Depth)
    const overlap = minDistance - currentDistance;

    // إذا كان هناك اختراق فعلي
    if (overlap > 0) {
      // نوزع مسافة الإبعاد بالتساوي على الكرتين (كل كرة تتراجع نصف المسافة)
      const move = overlap / 2;

      // تصحيح الزوايا (تقريب: القوس المقطوع ≈ الزاوية للقيم الصغيرة)
      // dθ = dx / L
      ball1.theta -= move / ball1.length; // دفع الكرة الأولى للخلف
      ball2.theta += move / ball2.length; // دفع الكرة الثانية للأمام
    }
  }
}
